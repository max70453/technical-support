if (!document.getElementById('chatModal')) {
    document.addEventListener('DOMContentLoaded', function() {
        var modal = document.getElementById('chatModal');
        if (modal) {
            modal.addEventListener('click', function(e) {
                if (e.target === modal) {
                    modal.style.display = 'none';
                }
            });
        }
        
        var sendBtn = document.getElementById('chatSendBtn');
        if (sendBtn) {
            sendBtn.addEventListener('click', sendMessage);
        }
        
        var chatInput = document.getElementById('chatInput');
        if (chatInput) {
            chatInput.addEventListener('keypress', function(e) {
                if (e.key === 'Enter') {
                    sendMessage();
                }
            });
        }
    });
}

function sendMessage() {
    var input = document.getElementById('chatInput');
    if (!input) return;
    
    var message = input.value.trim();
    if (!message) return;

    var messagesContainer = document.getElementById('chatMessages');
    var time = new Date().toLocaleTimeString('ru-RU', { hour: '2-digit', minute: '2-digit' });

    messagesContainer.innerHTML += '<div class="chat-message user"><div class="chat-bubble">' + message + '</div><div class="time">' + time + '</div></div>';

    input.value = '';
    messagesContainer.scrollTop = messagesContainer.scrollHeight;

    var response = getBotResponse(message.toLowerCase());
    
    setTimeout(function() {
        messagesContainer.innerHTML += '<div class="chat-message support"><div class="chat-bubble">' + response + '</div><div class="time">' + time + '</div></div>';
        messagesContainer.scrollTop = messagesContainer.scrollHeight;
    }, 1000 + Math.random() * 1000);
}

